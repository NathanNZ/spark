  test("MapType - insertion-order invariance") {
    val dt = MapType(StringType, LongType, valueContainsNull = false)
    def mapLit(pairs: (String, Long)*): Expression = {
      val keys = new GenericArrayData(pairs.map(p => UTF8String.fromString(p._1): Any).toArray)
      val vals = new GenericArrayData(pairs.map(p => p._2: Any).toArray)
      mkLit(new ArrayBasedMapData(keys, vals), dt)
    }
    val h1 = XxHash3(Seq(mapLit("a" -> 1L, "b" -> 2L, "c" -> 3L)), 0L).eval(null)
    val h2 = XxHash3(Seq(mapLit("c" -> 3L, "a" -> 1L, "b" -> 2L)), 0L).eval(null)
    val h3 = XxHash3(Seq(mapLit("b" -> 2L, "c" -> 3L, "a" -> 1L)), 0L).eval(null)
    assert(h1 == h2 && h1 == h3, "Map hash must be insertion-order independent")
    val hAlt = XxHash3(Seq(mapLit("a" -> 1L, "b" -> 99L, "c" -> 3L)), 0L).eval(null)
    assert(h1 != hAlt, "Maps with different values must hash differently")
  }

