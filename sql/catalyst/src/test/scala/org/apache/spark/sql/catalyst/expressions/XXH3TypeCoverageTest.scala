/**
 * Type-coverage regression tests for [[XxHash3]] (64-bit) and [[XxHash128]] (128-bit).
 *
 * Every Spark SQL type is exercised with representative values including nulls, arrays with
 * null/non-null elements, maps, and nested structs. The suite gives two guarantees per type:
 *
 *   1. **Codegen consistency** -- interpreted result == codegen result (ensured by
 *      [[ExpressionEvalHelper.checkEvaluation]] which runs both paths and compares).
 *   2. **Determinism** -- XxHash128 returns identical bytes when evaluated twice (catches
 *      misuse of mutable state in the codegen-generated path).
 *
 * Pinned golden values live in [[XXH3TypeCoverageGoldenPrinter.goldenXxHash3]] above.
 * To regenerate after an intentional algorithm change, run the printer's `main` method
 * and paste the output back into that map.
 */
class XXH3TypeCoverageTest extends SparkFunSuite with ExpressionEvalHelper {

  // ---------------------------------------------------------------------------
  // Helpers
  // ---------------------------------------------------------------------------

  private def check3(lit: Expression): Unit = {
    val expr = XxHash3(Seq(lit), 0L)
    checkEvaluation(expr, expr.eval(null))
  }

  private def check3seeded(lit: Expression, seed: Long): Unit = {
    val expr = XxHash3(Seq(lit), seed)
    checkEvaluation(expr, expr.eval(null))
  }

  private def check128(lit: Expression): Unit = {
    val expr = new XxHash128(Seq(lit))
    checkEvaluation(expr, expr.eval(null))
  }

  private def checkBoth(lit: Expression): Unit = { check3(lit); check128(lit) }

  private def mkLit[T](value: T, dt: DataType): Literal = Literal.create(value, dt)
  private def nullLit(dt: DataType): Literal = Literal.create(null, dt)

  // ---------------------------------------------------------------------------
  // Scalar types
  // ---------------------------------------------------------------------------

  test("NullType") {
    checkBoth(nullLit(NullType))
  }

  test("BooleanType") {
    checkBoth(mkLit(false, BooleanType))
    checkBoth(mkLit(true, BooleanType))
    checkBoth(nullLit(BooleanType))
  }

  test("ByteType") {
    Seq(0.toByte, 1.toByte, -1.toByte, Byte.MaxValue, Byte.MinValue).foreach { v =>
      checkBoth(mkLit(v, ByteType))
    }
    checkBoth(nullLit(ByteType))
  }

  test("ShortType") {
    Seq(0.toShort, 1.toShort, -1.toShort, Short.MaxValue, Short.MinValue).foreach { v =>
      checkBoth(mkLit(v, ShortType))
    }
    checkBoth(nullLit(ShortType))
  }

  test("IntegerType") {
    Seq(0, 1, -1, Int.MaxValue, Int.MinValue, 42).foreach { v =>
      checkBoth(mkLit(v, IntegerType))
    }
    checkBoth(nullLit(IntegerType))
  }

  test("LongType") {
    Seq(0L, 1L, -1L, Long.MaxValue, Long.MinValue, 1234567890123L).foreach { v =>
      checkBoth(mkLit(v, LongType))
    }
    checkBoth(nullLit(LongType))
  }

  test("FloatType") {
    Seq(0.0f, -0.0f, 1.5f, -1.5f, Float.NaN,
        Float.PositiveInfinity, Float.NegativeInfinity,
        Float.MaxValue, Float.MinPositiveValue).foreach { v =>
      checkBoth(mkLit(v, FloatType))
    }
    checkBoth(nullLit(FloatType))
  }

  test("DoubleType") {
    Seq(0.0, -0.0, 1.5, -1.5, Double.NaN,
        Double.PositiveInfinity, Double.NegativeInfinity,
        Double.MaxValue, Double.MinPositiveValue).foreach { v =>
      checkBoth(mkLit(v, DoubleType))
    }
    checkBoth(nullLit(DoubleType))
  }

  test("DecimalType - USER_DEFAULT (10,0)") {
    val dt = DecimalType.USER_DEFAULT
    Seq(Decimal(0), Decimal(1), Decimal(-1), Decimal(Int.MaxValue.toLong)).foreach { v =>
      checkBoth(mkLit(v, dt))
    }
    checkBoth(nullLit(dt))
  }

  test("DecimalType - SYSTEM_DEFAULT (38,18)") {
    val dt = DecimalType.SYSTEM_DEFAULT
    Seq(Decimal(0), Decimal("123456.789012345678"), Decimal("-9999999999999999999.999999999999999999"))
      .foreach { v => checkBoth(mkLit(v, dt)) }
    checkBoth(nullLit(dt))
  }

  test("DecimalType - various precisions and scales") {
    for ((prec, scale) <- Seq((10, 2), (18, 6), (38, 10))) {
      val dt = DecimalType(prec, scale)
      checkBoth(mkLit(Decimal(0), dt))
      checkBoth(mkLit(Decimal("12345.67"), dt))
      checkBoth(nullLit(dt))
    }
  }

  test("StringType") {
    val strs = Seq("", "hello world", "apache spark xxhash3",
                   "x" * 17,   // 17-128 byte path
                   "y" * 129,  // 129-240 byte path
                   "z" * 300)  // >240 byte long-input accumulator path
    strs.foreach { s =>
      checkBoth(mkLit(UTF8String.fromString(s), StringType))
    }
    checkBoth(nullLit(StringType))
  }

  test("BinaryType") {
    Seq(Array.empty[Byte],
