  // scalastyle:off println
  private def section(name: String): Unit = println(s"\n  // $name")

  private def emit(label: String, lit: Literal): Unit = {
    val h3: Any = XxHash3(Seq(lit), 0L).eval(null)
    val h128: Any = new XxHash128(Seq(lit)).eval(null)

    val h3str = h3 match {
      case l: Long => s"${l}L"
      case null    => "null /* XxHash3 should never return null */"
    }

    val h128str = h128 match {
      case null => "null"
      case bytes: Array[Byte] =>
        val buf = ByteBuffer.wrap(bytes).order(ByteOrder.BIG_ENDIAN)
        val hi = buf.getLong()
        val lo = buf.getLong()
        s"high=${hi}L low=${lo}L"
    }

    val key = s""""$label""""
    println(s"  ${key.padTo(COL, ' ')} -> ${h3str.padTo(26, ' ')}  // 128: $h128str")
  }
